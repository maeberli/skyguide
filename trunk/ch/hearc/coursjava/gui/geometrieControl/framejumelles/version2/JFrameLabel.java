	/*------------------------------*\
	|*				Get				*|
	\*------------------------------*/

	@Override
	public JLabel getComposant(int i)
		{
		return (JLabel)super.getComposant(i);
		}

	/*------------------------------------------------------------------*\
	|*							Methodes Private						*|
	\*------------------------------------------------------------------*/

