
package ch.hearc.coursjava.kitbase.args;

public class UseHelloArgs
	{

	/*------------------------------------------------------------------*\
	|*							Methodes Public							*|
	\*------------------------------------------------------------------*/

	public static void main(String[] args)
		{
		afficher(args);
		}

	private static void afficher(String[] tabString)
		{
		for(String elem:tabString)
			{
			System.out.println(elem);
			}
		}

	/*------------------------------------------------------------------*\
	|*							Methodes Private						*|
	\*------------------------------------------------------------------*/

	}
