
package ch.hearc.coursjava.poo.derivation.times;

public class UseTimes
	{

	/*------------------------------------------------------------------*\
	|*							Methodes Public							*|
	\*------------------------------------------------------------------*/

	public static void main(String[] args)
		{
		main();
		}

	public static void main()
		{
		HmsmTimes hmsmTimes = new HmsmTimes(1,2,3,4);
		System.out.println(hmsmTimes.toString());

		hmsmTimes.set(10, 20, 30, 40);
		System.out.println(hmsmTimes.toString());

		HmsmTimes hmsmTimesClone = new HmsmTimes(hmsmTimes);
		System.out.println(hmsmTimesClone.toString());

		hmsmTimes.set(100, 200, 300, 400);
		System.out.println(hmsmTimes.toString());
		System.out.println(hmsmTimesClone.toString());
		}

	/*------------------------------------------------------------------*\
	|*							Methodes Private						*|
	\*------------------------------------------------------------------*/

	}
