
package ch.hearc.coursjava.poo.thread.additionvectorielle;

import junit.framework.Assert;

public class Interval
	{

	/*------------------------------------------------------------------*\
	|*							Constructeurs							*|
	\*------------------------------------------------------------------*/

	/**
	 * Hyp : iStart <= iStop
	 */
	public Interval(int iStart, int iStop)
		{
		Assert.assertTrue(iStart <= iStop);
		this.iStart = iStart;
		this.iStop = iStop;
		}

	/*------------------------------------------------------------------*\
	|*							Methodes Public							*|
	\*------------------------------------------------------------------*/

	/*------------------------------*\
	|*				Set				*|
	\*------------------------------*/

	/*------------------------------*\
	|*				Get				*|
	\*------------------------------*/

	public int getiStart()
		{
		return this.iStart;
		}

	public int getiStop()
		{
		return this.iStop;
		}

	/*------------------------------------------------------------------*\
	|*							Methodes Private						*|
	\*------------------------------------------------------------------*/

	/*------------------------------------------------------------------*\
	|*							Attributs Private						*|
	\*------------------------------------------------------------------*/
	//Inputs
	private int iStart;
	private int iStop;

	}
